
public class Ex01_01_출력문 {
	public static void main(String[] args) {
		System.out.println(123); //괄호안을 출력
		System.out.println("가나다");
		System.out.println('a'); //한글자는 작은 따옴표 가능
		System.out.println("123"); //""가 있으면 숫자가 아닌 문자 취급	
	}
}

