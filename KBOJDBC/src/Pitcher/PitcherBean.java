package Pitcher;
public class PitcherBean {
	
	private int pno;
	private String club;
	private String pplayer; 
	private int pnum;
	private String pname;
	private double era;
	private int strike;
	
	//생성자
	public PitcherBean() {
		super();
	}
	
	public PitcherBean(int pno, String club, String pplayer, int pnum, String pname, double era, int strike) {
		this.pno = pno;
		this.club = club;
		this.pplayer = pplayer;
		this.pnum = pnum;
		this.pname = pname;
		this.era = era;
		this.strike = strike;
	}

	//setter
	public void setPno(int pno) {
		this.pno = pno;
	}
	public void setClub(String club) {
		this.club = club;
	}
	public void setPplayer(String pplayer) {
		this.pplayer = pplayer;
	}
	public void setPnum(int pnum) {
		this.pnum = pnum;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public void setEra(double era) {
		this.era = era;
	}
	public void setStrike(int strike) {
		this.strike = strike;
	}
	
	
	//getter
	public int getPno() {
		return pno;
	}
	public String getClub() {
		return club;
	}
	public String getPplayer() {
		return pplayer;
	}
	public int getPnum() {
		return pnum;
	}
	public String getPname() {
		return pname;
	}
	public double getEra() {
		return era;
	}
	public int getStrike() {
		return strike;
	}
	
}
